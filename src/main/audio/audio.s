SECTION "SFXFunctions", ROM0

; Make a menu transition SFX
MenuTransitionSound::

    ; -- generated by export-sfx.py --
    ld a, hUGE_CH1_MASK
    ld [hUGE_MutedChannels], a
    ld a, $37
    ld [rNR10], a
    ld a, $80
    ld [rNR11], a
    ld a, $f1
    ld [rNR12], a
    ld a, $ba
    ld [rNR13], a
    ld a, $87
    ld [rNR14], a

    ld a, 10   
    call WaitForFrames

    xor a
    ld [hUGE_MutedChannels], a

    ret

